import pygame

# ----------------------------------------------------------------------------------------------------------------------------------------------
# ---------------------------------------------------------- Class Boutton ------------------------------------------------------------------------
# ----------------------------------------------------------------------------------------------------------------------------------------------

class Button(object):
	def __init__(self, txt):
		self._text = txt
		self.default_color = (0,255,0)
		self.font_color = (0,0,0)
		self.is_start = False
		self.obj = None

#------------------------------------------------------------------------------------------------------------------------------------------------
#----------------------------------------------------------- GETTERS / SETTERS ------------------------------------------------------------------
#------------------------------------------------------------------------------------------------------------------------------------------------


	def _set_text(self,txt):
		self._text=txt

	def _get_text(self):
		return self._text

#------------------------------------------------------------------------------------------------------------------------------------------------
#------------------------------------------------------------------------------------------------------------------------------------------------
#------------------------------------------------------------------------------------------------------------------------------------------------

	# switch boolean
	def change_started_bool(self):
		if self.is_start:
			self.is_start=False
		else:
			self.is_start=True

#------------------------------------------------------------------------------------------------------------------------------------------------
	
	#v√©rifie si on a appuye sur le bouton (si la simulation est lance)
	def check_is_started(self):
		return self.is_start

#------------------------------------------------------------------------------------------------------------------------------------------------

	#change la couleur du texte
	def change_font_color(self,clr):
		self.font_color=clr

#------------------------------------------------------------------------------------------------------------------------------------------------

	#switch rouge / vert

	def change_appearance(self):
		if self.is_start:
			self.default_color=(255,0,0)
			self._text="STOP"
		else:
			self.default_color = (0,255,0)
			self._text="START"

#------------------------------------------------------------------------------------------------------------------------------------------------
	
	#change la couleur du bouton
	def change_default_color(self,clr):
		self.default_color=clr

#------------------------------------------------------------------------------------------------------------------------------------------------
	#initialise la police du texte
	def label(self,size):
   		font = pygame.font.Font(None, size)
   		return font.render(self._text, 1, self.font_color)

#------------------------------------------------------------------------------------------------------------------------------------------------
	# affiche le bouton dans la fenetre pygame
	def draw(self, screen, rectcoord, labelcoord,size):
		self.obj  = pygame.draw.rect(screen, self.default_color, rectcoord)
		pygame.draw.rect(screen,(0,0,0),rectcoord,3)
		margin =0
		screen.blit(self.label(size), labelcoord)

